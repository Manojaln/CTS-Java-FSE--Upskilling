package com.data.types;

public class PrimitiveDatatypes {
	public static void main(String agrs[]) {
		int a1= 10;
		float b2=12.123456f;
		double c3 = 13.23456789876543d;
		char d4= 'D';
		boolean e5 = true;
		
		System.out.println("value of int : "+ a1);
		System.out.println("value of float : "+ b2);
		System.out.println("value of double : "+ c3);
		System.out.println("value of char : "+ d4);
		System.out.println("value of boolean : "+ e5);
	}
}
